    private Produto encontrarProduto(int codigo) {
        Produto produtoEncontrado = null;
        for (Produto p : produtos) {
            if (p.getCodigo() == codigo) {
                produtoEncontrado = p;
                break;
            }
        };
        
        return produtoEncontrado;
    }
    
