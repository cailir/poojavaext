    private void jTextField1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField1FocusLost
        Produto p = encontrarProduto(Integer.parseInt(jTextField1.getText()));
        jLabel7.setText(p.getDescricao());
        jLabel4.setText(String.format("R$ %.2f", p.getValorVenda()));
        jLabel5.setText(String.format("R$ %.2f", p.getValorVenda() * Integer.parseInt(jTextField2.getText())));
        
        produtosNaVenda.add(p);
        
        this.revalidate();
        this.repaint();
    }//GEN-LAST:event_jTextField1FocusLost

    private void jTextField2FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField2FocusLost
        if (Integer.parseInt(jTextField2.getText()) <= 0) {
            jTextField2.setText("1");
        }
        Produto p = encontrarProduto(Integer.parseInt(jTextField1.getText()));
        
        jLabel5.setText(String.format("R$ %.2f", p.getValorVenda() * Integer.parseInt(jTextField2.getText())));
        
        this.revalidate();
        this.repaint();
    }//GEN-LAST:event_jTextField2FocusLost

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        Produto p = encontrarProduto(Integer.parseInt(jTextField1.getText()));
        
        DefaultListModel<String> list = new DefaultListModel<>();
        for (int i = 0; i < jList1.getModel().getSize(); i++) {
            list.addElement(jList1.getModel().getElementAt(i));
        }
        
        list.addElement(String.format("%s -- R$ %.2f %dx", p.getDescricao(), p.getValorVenda(), Integer.parseInt(jTextField2.getText())));
        
        jList1.setModel(list);
        
        subtotal += Integer.parseInt(jTextField2.getText()) * p.getValorVenda();
        
        jLabel9.setText(String.format("Subtotal: R$ %.2f", subtotal));
        
        for (int i = 0; i < Integer.parseInt(jTextField2.getText())-1; i++) {
            produtosNaVenda.add(p);
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        Venda venda = new Venda(conexaoMySQL, produtosNaVenda);
        mainFrame.adicionarVenda(venda);
        
        subtotal = 0;
        jLabel9.setText("Subtotal: R$ 0,00");
                
        jList1.setModel(new DefaultListModel());
    }//GEN-LAST:event_jButton2MouseClicked


